<script>
  function top() {
    document.body.scrollTop = 0; // For Safari
    document.documentElement.scrollTop = 0; // For Chrome, Firefox, IE and Opera
  }
</script>

<center><button onclick={top}>Click to Resurface</button></center>

<style>
    button:hover {
        cursor: pointer;
    }
    button {
        margin: 2em;
    }
</style>
